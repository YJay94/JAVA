public class Human {

        int num;
        String name;
        int old;
        int height;

        public int getNum() {
                return num;
        }

        public void setNum(int num) {
                this.num = num;
        }

        public String getName() {
                return name;
        }

        public void setName(String name) {
                this.name = name;
        }

        public int getOld() {
                return old;
        }

        public void setOld(int old) {
                this.old = old;
        }

        public int getHeight() {
                return height;
        }

        public void setHeight(int height) {
                this.height = height;
        }
}
